package com.example.lenovo.jiyiji.PieChart;

public class ArcBean {

    private float value;//以什么值来做角度
    private int color;
    private int radius;

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }
}
